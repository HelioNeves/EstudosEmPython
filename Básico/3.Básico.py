
''' COMPARADORES
1. Igual á (==)
2. Diferente de (!=)
3. Menor que (<)
4. Menor ou igual á (<=)
5. Maior que (>)
6. Maior ou igual á (>=) '''

# A. Testando comparadores
print("\n A.")
print((3 > 6), (3 < 6),
(3 == 6), (3 != 6))

''' OPERADORES BOOLEANOS
1. Não (not)
2. E (and)
3. Ou (or)  '''

# B. Operadores booleanos
print("\n B.")
print(not (3 > 6) and (3 < 6),
(3 == 6) or (3 != 6))
