
''' CONDICIONAIS
Sintaxe if, elif e else '''

# A. IF
nome = "Gabriel"

if nome == "Gabriel":
    print("\n A.")
    print(nome == "Gabriel")

# B. ELSE IF (elif)
nome = "Gabrielle"

if nome == "Gabriel":
    print("\n B.")
    print(nome == "Gabriel")
elif nome == "Gabrielle":
    print("\n B.")
    print(nome == "Gabrielle")

# C. ELSE
nome = "Gabrielle"

if nome == "Gabriel":
    print("\n C.")
    print(nome == "Gabriel")
else:
    print("\n C.")
    print(nome == "Gabriel")
